  <br /><p>Malayali Cultural Association established 24 years ago, has been the fraternity of malayalam speaking students and staff of IIT-Bombay ever since. The league promotes literary and arty-crafty talents of its members and as well abates the reclusiveness one might find in his new campus life. All the new malayalee joiners of the campus are requested to join the association and participate in all its functions and make it more illustrious.</p><br />
    <p>You can join our family by registering yourself on this site. Once you are registered you will have access to the privileges of the association. All the new events and notifications will be announced here. Only a registered member can avail the library and server owned by the association which has a huge collection of malayalam books, films, songs, videos and pictures, thanks to ardent efforts of the members.</p>
   	
